<script>
    // Получаем все элементы с классом "buyers"
    var buyers = document.querySelectorAll('.buyers');

    // Перебираем каждый элемент с классом "buyers"
    buyers.forEach(function(buyer) {
        // Назначаем обработчик события "click"
        buyer.addEventListener('click', function() {
            // Находим родительский элемент с классом "d-block"
            var dBlock = buyer.closest('.d-block');

            // Находим все элементы с классом "hidden" внутри родительского элемента "d-block"
            var hiddenElements = dBlock.getElementsByClassName('hidden');

            // Находим иконку внутри элемента с классом "arrow-icon"
            var arrowIcon = buyer.querySelector('.arrow-icon');

            // Переключаем видимость всех найденных элементов с классом "hidden"
            for (var i = 0; i < hiddenElements.length; i++) {
                var hidden = hiddenElements[i];
                if (hidden.style.display === 'none' || hidden.style.display === '') {
                    hidden.style.display = 'flex';
                    // Здесь меняем иконку на "fas fa-chevron-down" при открытии
                    arrowIcon.classList.remove('fa-chevron-right');
                    arrowIcon.classList.add('fa-chevron-down');
                } else {
                    hidden.style.display = 'none';
                    // Здесь меняем иконку на "fas fa-chevron-right" при закрытии
                    arrowIcon.classList.remove('fa-chevron-down');
                    arrowIcon.classList.add('fa-chevron-right');
                }
            }
        });
    });
</script>

